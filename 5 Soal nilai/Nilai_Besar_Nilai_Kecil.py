
a = int(input("Masukkan nilai a: "))
b = int(input("Masukkan nilai b: "))
c = int(input("Masukkan nilai c: "))

letik = min(a, b, c)
gede = max(a, b, c)

print("Nilai terkecil:", letik)
print("Nilai terbesar:", gede)
print("Nilai a:", a)
print("Nilai b:", b)
print("Nilai c:", c)

# "min" untuk menemukan nilai terkecilnya
# "max" untuk menemukan nilai terbesarnya