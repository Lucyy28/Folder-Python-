diagonal_1 = float(input("Masukkan panjang diagonal pertama belah ketupat: "))
diagonal_2 = float(input("Masukkan panjang diagonal kedua belah ketupat: "))
sisi = float(input("Masukkan panjang sisi belah ketupat: "))

luas = 0.5 * diagonal_1 * diagonal_2
keliling = 4 * sisi

print(f"Luas belah ketupat adalah: {luas}")
print(f"Keliling belah ketupat adalah: {keliling}")
